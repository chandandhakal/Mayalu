const defaultLocale = 'en';

export function t(locale, key) {
  if (!locale || !key) return key || '';
  const localeData = translations[locale];
  if (localeData && localeData[key] !== undefined) return localeData[key];
  if (translations[defaultLocale][key] !== undefined) return translations[defaultLocale][key];
  return key;
}

